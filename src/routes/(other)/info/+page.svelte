<Heading heading="Welcome to Coffee Bibber!" />
<p>
	We're excited to have you join our growing community of coffee lovers. This platform is designed
	to be a comprehensive resource for high-end artisan coffees, showcasing the best cultivars,
	producers, and roasters from around the world. To help you make the most of your experience, we've
	put together this guide to walk you through the process of contributing to our coffee knowledge
	base.
</p>
<h5>Getting Started: Adding Tasting Notes</h5>
<p>
	If you'd like to add a tasting note for a coffee that's not yet on our site, you'll need to follow
	a series of steps to ensure all the necessary information is in place. Here's a step-by-step guide
	to help you through the process:
</p>
<h6>Add a Cultivar</h6>
<p>
	If the coffee's cultivar isn't listed on our site, you'll need to add it first. Navigate to the
	"Cultivars" section and click "Add New Cultivar." Fill out the required information, including the
	name and characteristics of the cultivar. Submit the form to add the new cultivar to our database.
</p>
<h6>Add a Producer</h6>
<p>
	If the coffee's producer isn't listed, head to the "Producers" section. Click "Add New Producer"
	and provide the necessary details about the producer, such as their name, location, and any
	relevant background information. Submit the form to include the new producer in our directory.
</p>
<h6>Link the Cultivar to a Producer</h6>
<p>
	In the "Producers Cultivars" section, click "Add New Producer Cultivar." Choose the producer
	you've just added, and select the cultivar you want to link to them. If applicable, provide the
	harvest date. Submit the form to create a connection between the producer and the cultivar.
</p>
<h6>Add a Roaster</h6>
<p>
	If the roaster responsible for the coffee isn't listed, go to the "Roasters" section. Click "Add
	New Roaster" and fill in the details about the roaster, such as their name, location, and roasting
	philosophy. Submit the form to add the new roaster to our database.
</p>
<h6>Create a Coffee</h6>
<p>
	With the cultivar, producer, and roaster in place, you can now create a new coffee entry. Go to
	the "Coffees" section and click "Add New Coffee." Choose the roaster, producer, and cultivar from
	the dropdown menus. Submit the form to create the new coffee listing.
</p>
<h6>Add a Tasting Note</h6>
<p>
	Finally, you're ready to add a tasting note for the coffee you've just created. Head to the
	"Tasting Notes" section and click "Add New Tasting Note." Choose the coffee you've added, and
	provide your tasting notes, including flavor profiles, aroma, and any other observations. Submit
	the form to share your tasting notes with our community.
</p>
<p>
	Congratulations! You've successfully contributed to our growing coffee knowledge base. We
	appreciate your effort in sharing your insights with fellow coffee enthusiasts. As you continue to
	explore and enjoy artisan coffees, we encourage you to share more tasting notes and help build a
	comprehensive resource for the world's best coffees. Happy tasting!
</p>
